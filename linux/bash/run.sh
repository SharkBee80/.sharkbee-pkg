#!/bin/bash

# 检查当前用户是否为 root
if [ "$EUID" -ne 0 ]; then
	echo "此脚本需要管理员权限，正在尝试切换..."
	# 自动使用 sudo 重新运行当前脚本，并传递所有原本的参数
	exec sudo "$0" "$@"
else
	echo "当前已成功以 root 权限运行！"
fi

# ==========================================
# 下方编写你需要 root 权限才能执行的代码
# ==========================================

date

read -r
